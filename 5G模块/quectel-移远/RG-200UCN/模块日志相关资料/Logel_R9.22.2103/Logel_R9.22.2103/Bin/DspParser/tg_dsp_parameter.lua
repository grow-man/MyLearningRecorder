init_dsp_parameter_table=
{
    [0]='tg_parameter_info'
}

-- main interface function, load by C functions -------------------------------------
-- NOTE: !!!!!!!!!!!!!!!!!!!!DON'T CHANGE THIS FUNCTION!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!

function dsp_parameter(paraName)
	return tg_parameter_info[paraName]                  --tg dsp
end




